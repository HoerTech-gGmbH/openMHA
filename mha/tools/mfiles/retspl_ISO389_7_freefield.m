function [f,c] = retspl_ISO389_7_freefield
% reference equivalent threshold sound pressure level
%
% -- abstract: --
% ISO 389-7:2005 specifies a reference threshold of hearing for the
% calibration of audiometric equipment used under the following
% conditions.
%
%   1. The sound field in the absence of the listener consists of
%   either a free progressive plane wave (free field) or a diffuse
%   sound field, as specified in ISO 8253-2. In the case of a free
%   field, the source of sound is directly in front of the listener
%   (frontal incidence).
%
%   2. The sound signals are pure (sinusoidal) tones in the case of
%   free-field conditions and one-third octave bands of (white or
%   pink) noise in the case of diffuse-field conditions.
%
%   3. The sound pressure level is measured in the absence of the
%   listener at the position where the centre of the listener's
%   head would be.
%
%   4. Listening is binaural.
%
% The data in ISO 389-7:2005 are given in numerical form for the
% preferred frequencies in the one-third octave series from 20 Hz
% to 16 000 Hz inclusive in accordance with ISO 266 and, in
% addition, for some intermediate audiometric frequencies up to 18000 Hz.
%
% The threshold data differ from the audiometric zero specified in
% ISO 389-1, ISO 389-2, ISO/TR 389-5 and ISO 389-8, since the
% latter refer to monaural listening through earphones with sound
% pressure levels referred to specified couplers and ear
% simulators. Direct comparison between the data in the parts of
% ISO 389 mentioned above and that in ISO 389-7:2005 is therefore
% not appropriate.

  data =  ...
      [20 78.5   ;...
       25 68.7   ;...
       31.5 59.5 ;...
       40 51.1   ;...
       50 44.0   ;...
       63 37.5   ;...
       80 31.5   ;...
       100 26.5  ;...
       125 22.1  ;...
       160 17.9  ;...
       200 14.4  ;...
       250 11.4  ;...
       315 8.6   ;...
       400 6.2   ;...
       500 4.4   ;...
       630 3.0   ;...
       750 2.4   ;...
       800 2.2   ;...
       1000 2.4  ;...
       1250 3.5  ;...
       1500 2.4  ;...
       1600 1.7  ;...
       2000 -1.3 ;...
       2500 -4.2 ;...
       3000 -5.8 ;...
       3150 -6.0 ;...
       4000 -5.4 ;...
       5000 -1.5 ;...
       6000 4.3  ;...
       6300 6.0  ;...
       8000 12.6 ;...
       9000 13.9 ;...
       10000 13.9 ;...
       11200 13.0 ;...
       12500 12.3 ;...
       14000 18.4 ;...
       16000 40.2 ;...
       18000 73.2];

  f = data(:,1);
  c = data(:,2);
end
